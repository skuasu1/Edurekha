package EssentialsJava;

public class IfNumberPositiveorNot {

	public static void main(String[] args) {
		double i = 0;
		if (i > 0) {
			System.out.println("Passed number is a positive number");
		} else if (i == 0) {
			System.out.println("Passed number is a zero number");
		} else {
			System.out.println("Passed number is a negative number");
		}

	}

}
